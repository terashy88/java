package talana;

/**
 * Math cheatsheet
 */

public class Math {

    public static void main(String[] args) {

        int num = 3;
        int num2 = 6;

        System.out.println(num + num2); // Plus
        System.out.println(num - num2); // Minus
        System.out.println(num * num2); // Multiplication
        System.out.println(num / num2); // Division
        System.out.println(num2 % num); // Modular multiplication
        System.out.println(num2 % num + 5); // Modular multiplication

        System.out.println(4 ^ 2); // XOR operation
    }
}
