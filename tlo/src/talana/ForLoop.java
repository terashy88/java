package talana;

/**
 * forLoop
 */
public class ForLoop {

    public static void main(String[] args) {

        int n = 3;
        int n2 = 0;

        System.out.println("counting!");

        for (int i = 0; i <= n; i++) {
            System.out.println("round: " + i);

        }

        for (int e = 3; e >= n2; e--) {
            System.out.println("round e: " + e);

        }
    }
}
