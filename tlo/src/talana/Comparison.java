package talana;

/**
 * Comparison Operators
 */
public class Comparison {

    public static void main(String[] args) {
        // Age Comparison
        int meli = 19;
        int sara = 18;

        System.out.println(meli > sara);
        System.out.println(meli >= sara);
        System.out.println(meli < sara);
        System.out.println(meli == sara);
        System.out.println(meli != sara);
    }
}
